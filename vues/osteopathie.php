            <h1>Qu'est ce que l'Ostéopathie ?</h1>
            <br>
            <div style="text-align: justify;">
                <p>
                    "L'Ostéopathie est l'art de diagnostiquer et de traiter par la main les dysfonctions de micro mobilité
                    des tissus du corps entrainant des troubles <strong>fonctionnels</strong> (en opposition aux troubles <strong>organiques</strong>) pouvant perturber l'état de santé"
                </p>
                <p>

                    Elle se réfère à l'anatomie et à la physiologie. Elle repose sur des compétences spécifiques, une connaissance approfondie du corps humain et des interactions
                    entre chacun de ses composants.
                </p>
                <p>
                    Cette discipline permet de réajuster, grâce à des manipulations précises et spécifiques, l'équilibre du corps. En effet tout déséquilibre de sa structure
                    ou perturbation provoque immanquablement des troubles progressifs de sa mécanique interne, qui entraîneront dans un premier temps des adaptations qui pourront plus tard être à l'origine de la douleur.
                </p>
                <p>
                    L'Ostéopathie est une thérapie de première intention, cela veut dire que vous pouvez venir consulter directement sans prescription médicale.
                    Il est cependant idéal de venir aux rendez-vous avec les derniers examens complémentaires effectués.
                </p>
                <p>
                    Cette discipline s'inscrit dans une démarche de complémentarité avec les autres professionnels de santé tels que les médecins, les kinésithérapeutes,
                    les podologues, les sages-femmes, les infirmières voire même les ophtalmologistes, dentistes ou encore orthoptistes.
                </p>
            </div>
